    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'french_video' => 'required_without:dutch_video|mimetypes:video/x-ms-asf,video/x-flv,video/mp4,application/x-mpegURL,video/MP2T,video/3gpp,video/quicktime,video/x-msvideo,video/x-ms-wmv,video/avi',
            'dutch_video' => 'required_without:french_video|mimetypes:video/x-ms-asf,video/x-flv,video/mp4,application/x-mpegURL,video/MP2T,video/3gpp,video/quicktime,video/x-msvideo,video/x-ms-wmv,video/avi'
//            'mimetypes:video/x-ms-asf,video/x-flv,video/mp4,application/x-mpegURL,video/MP2T,video/3gpp,video/quicktime,video/x-msvideo,video/x-ms-wmv,video/avi'
        ]);

        foreach(['dutch_video', 'french_video'] as $video) {
            if($request->hasFile($video)) {
                $file = $request->file($video);
                $lang = explode('_', $video)[0];
                if($prev_video = Video::where('lang', $lang)->first())
                    File::delete(public_path() . '/videos/' . $prev_video->dest);

                $request->file($video)->move(public_path() . '/videos/', $video . '.' . $file->getClientOriginalExtension());

                Video::updateOrCreate(['lang' => $lang], ['dest' => $video . '.' . $file->getClientOriginalExtension()]);
            }
        }

        return redirect('home');
    }

